    private void reportButtonClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_reportButtonClicked
       
       String newDisasterType = null;
       String newDisasterLocation = null;
       String newDisasterDate = null;
       String newDisasterDescription = null;
        
       JOptionPane.showMessageDialog(null, "Hi, " + userData.initUserData.name + "! Thank you for showing interest in contributing to the list of disasters. \n\nPlease click 'OK' to continue with answering some questions about the disaster."  , "Report A Disaster", 1);
       
       while(true) {
            newDisasterType = JOptionPane.showInputDialog(null, "Please enter the type of the disaster experienced (eg- Tsunami) : ", "Disaster Type", 3);
            
            if(newDisasterType.isEmpty())
               JOptionPane.showMessageDialog(null, "Please enter a valid disaster type!", "Error", 2);
            
            else 
               break;
       }
       
       while(true) {
       
           newDisasterLocation = JOptionPane.showInputDialog(null, "Please enter the location of the given "+ newDisasterType+ " (eg- Penang | Malaysia) : ", "Disaster Location", 3);
       
           if(newDisasterLocation.isEmpty())
               JOptionPane.showMessageDialog(null, "Please enter a valid disaster location!", "Error", 2);
            
            else 
               break;
       }
       
       while(true) {
       
           newDisasterDate = JOptionPane.showInputDialog(null, "Please enter the date of the "+ newDisasterType + " taken place in "+ newDisasterLocation + " (eg- 18 February 2008) : ", "Disaster Date", 3 );
       
           if(newDisasterDate.isEmpty())
               JOptionPane.showMessageDialog(null, "Please enter a valid disaster date!", "Error", 2);
            
            else 
               break;
       }
       
       while(true) {
           newDisasterDescription = JOptionPane.showInputDialog(null, "Please enter some details of the " + newDisasterType + " happened in "+ newDisasterLocation + " on " + newDisasterDate +" : ", "Title", 3);
       
           if(newDisasterDescription.isEmpty())
               JOptionPane.showMessageDialog(null, "Please enter a valid disaster description!", "Error", 2);
            
            else 
               break;
       }
       
       JOptionPane.showMessageDialog(null, "Thank you for your input!\nYour input will be verified by our admins and updated on our system shortly.", "Report A Disaster", 1);
       
       NewlyReportedDisaster userReport = new NewlyReportedDisaster(userData.initUserData.name, newDisasterType, newDisasterLocation, newDisasterDate, newDisasterDescription);
       newlyReportedDisastersList.newlyReportedDisasterList.add(userReport);
       userData.initUserData.numberOfReport++;
       
       screens.UserLoginScreen.setUserHomeScreenData();
    }//GEN-LAST:event_reportButtonClicked

