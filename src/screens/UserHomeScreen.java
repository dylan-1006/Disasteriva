    public static void fetchAndSetDisasterDetailScreen(int listIndex) {
            
        DisasterDetailScreen disasterDetailScreen = new DisasterDetailScreen();
        
        //Image
        DisasterDetailScreen.disasterDetailImage.setIcon(new javax.swing.ImageIcon(disasterList.class.getResource(disasterList.disasterInitList.get(listIndex).imageUrl_landscape)));
        
        //Title
        DisasterDetailScreen.disasterDetailType.setText("<html><p>" + disasterList.disasterInitList.get(listIndex).type + "</p></html>");
        
        //Info
        String location = disasterList.disasterInitList.get(listIndex).location;
        String date = disasterList.disasterInitList.get(listIndex).date;
        DisasterDetailScreen.disasterDetailInfo.setText(location + ", " + date);
        
        //Donation
        String currentDonationAmount = String.valueOf(disasterList.disasterInitList.get(listIndex).donationCollection);
        String donationGoalAmount = String.valueOf(disasterList.disasterInitList.get(listIndex).donationGoal);
        double donationPercentage = (disasterList.disasterInitList.get(listIndex).donationCollection/disasterList.disasterInitList.get(listIndex).donationGoal*100.00);
        String donationPercentageString = String.valueOf(Math.floor(donationPercentage));
        DisasterDetailScreen.disasterDetailDonation.setText(currentDonationAmount + " / " + donationGoalAmount +" (" + donationPercentageString + "%)"); 
        
        //Description
        String disasterDescription = disasterList.disasterInitList.get(listIndex).description;
        DisasterDetailScreen.disasterDetailDescription.setText("<html><p>"+disasterDescription+"</p></html>");
        
        //Selected Index
        DisasterDetailScreen.selectedDisasterListIndex = listIndex;
        
        disasterDetailScreen.setVisible(true);
    }
    
    
